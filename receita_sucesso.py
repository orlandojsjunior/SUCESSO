def success(dedication, persistence, passion):
    dedication += 1    # Dedicação: Aumente sua dedicação em pelo menos um ponto.
    persistence += 1   # Persistência: Seja um pouco mais persistente a cada dia.
    passion = True     # Paixão Verdadeira: Encontre e mantenha a verdadeira paixão no que faz.
    
    if passion == True:
        magic = dedication + persistence
        return magic # Com esses ingredientes, você pode criar sua própria magia do sucesso! Lembre-se, a paixão é a chave para ativar a fórmula.
    else: 
        magic = 0
        return magic

